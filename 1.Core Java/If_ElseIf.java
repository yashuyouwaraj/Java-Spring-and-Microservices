public class If_ElseIf {
    public static void main(String[] args) {
        int number = -10;

        //If-Else-If Statement
        if(number>0){
            System.out.println("The number is positive.");
        } else if(number<0){
            System.out.println("The number is negative.");
        } else {
            System.out.println("The number is zero.");
        }
    }
}
